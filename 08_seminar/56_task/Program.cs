// Задача 56: Задайте прямоугольный двумерный массив. Напишите программу, которая будет находить 
// строку с наименьшей суммой элементов.
// Например, задан массив:
// 1 4 7 2
// 5 9 2 3
// 8 4 2 4
// 5 2 6 7
// Программа считает сумму элементов в каждой строке и выдаёт номер строки с наименьшей суммой элементов: 1 строка

// Задаем прямоугольный двумерный массив
int[,] matrix = {
    { 1, 4, 7, 2 },
    { 5, 9, 2, 3 },
    { 8, 4, 2, 4 },
    { 5, 2, 6, 7 }
};

// Находим строку с наименьшей суммой элементов
int minRow = FindRowWithMinSum(matrix);

// Выводим результат
Console.WriteLine($"Строка с наименьшей суммой элементов: {minRow + 1}");

int FindRowWithMinSum(int[,] matrix)
{
    int minSum = int.MaxValue; // Начальное значение для минимальной суммы
    int minRow = 0; // Номер строки с наименьшей суммой

    // Проходим по каждой строке матрицы
    for (int i = 0; i < matrix.GetLength(0); i++)
    {
        int currentSum = 0; // Текущая сумма элементов строки

        // Считаем сумму элементов строки
        for (int j = 0; j < matrix.GetLength(1); j++)
        {
            currentSum += matrix[i, j];
        }

        // Сравниваем сумму текущей строки с минимальной
        if (currentSum < minSum)
        {
            minSum = currentSum;
            minRow = i;
        }
    }

    // Возвращаем номер строки с наименьшей суммой элементов
    return minRow;
}




